/**
 * This enumeration contains all categories of activities. Its primary purpose is to group activities. Those groups
 * are then used to facilitate the selection of relevant activities in different places of the model.
 */
public enum ActivityCategory {
	LEISURE,
	WORK,
	PERSONAL_CARE,
	HOUSEHOLD_AND_FAMILY_CARE,
	TRAVEL,
	IDLE
}
